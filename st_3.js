// SUN
function f1() {
    var n = document.getElementById("given_weight").value;
    n = Number(n);
    if (isNaN(n) || n == ' ') {
        alert("Please insert a number");
    } else {
        let gmer = 27.01;

        let newmass = n * gmer;
        document.getElementById("new_weight").innerHTML = newmass;
        document.getElementById("name_planet").innerHTML = "Sun";
    }
}
